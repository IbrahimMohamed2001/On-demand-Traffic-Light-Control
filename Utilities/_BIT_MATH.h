#ifndef _BIT_MATH_H_
#define _BIT_MATH_H_

/************************************************************************/
/*                            Clearing a bit                            */
/************************************************************************/

#define CLR_BIT(REG_VAL,BITNO) (REG_VAL) &= ~(1 << (BITNO))

/************************************************************************/
/*                            Setting a bit                             */
/************************************************************************/

#define SET_BIT(REG_VAL,BITNO) (REG_VAL) |=  (1 << (BITNO))

/************************************************************************/
/*                            Toggling a bit                            */
/************************************************************************/

#define TOG_BIT(REG_VAL,BITNO) (REG_VAL) ^=  (1 << (BITNO))

/************************************************************************/
/*                            Reading a bit                             */
/************************************************************************/

#define READ_BIT(REG_VAL,BITNO) (((REG_VAL) & (1 << (BITNO))) >> (BITNO))

#endif /* _BIT_MATH_H_ */